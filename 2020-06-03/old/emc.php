<h1>Evolving Musical Counterpoint</h1>

<p>During the spring semester of 2011 I developed the Chronopoint Musical Evolution System, a Genetic Algorithm for evolving musical counterpoint,
under the supervision of <a href="http://www.cs.umd.edu/~reggia/">Dr. James Reggia</a> of University of Maryland's 
<a href="http://www.cs.umd.edu/~reggia/home/group.html">Biologically-Inspired Computing Lab</a>. Chronopoint can serve as both a
compositional aid for active composers or as a pedagogical tool for students hoping to learn how a Genetic Algorithm works in an interactive fashion.
Resources relating to the project can be found below.

