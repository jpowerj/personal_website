<h1>Parallel Processing for Brain-Computer Interfaces</h1>

<p>Over the summer of 2010, I worked in <a href="http://www.jacobsschool.ucsd.edu/faculty/faculty_bios/index.sfe?fmp_recid=331">Dr. Todd P. Coleman</a>'s
 <a href="http://bmi.beckman.illinois.edu/index.html">Brain-Computer Interface Laboratory</a>
at the <a href="http://illinois.edu/">University of Illinois at Urbana-Champaign</a>.
The lab was attempting to allow quadriplegic subjects the ability to type on a computer and control vehicles via an electrode cap placed on their scalp.
In particular, I utilized the Graphical Processing Units on a set of new computers the lab had just received to parallelize their signal processing
code. By the end of the summer I was able to achieve speedups of up to 800% on the group's MATLAB code, and I presented my findings to the general
public at an on-campus conference. Some resources relating to this project can be found below.

