<h1>Wireless Network Optimization</h1>

<p>Over the summer of 2011, I participated in a Research Experience for Undergraduates at the <a href="http://www.ust.hk/eng/index.htm">Hong Kong University of Science and Technology</a>,
sponsored by the <a href="http://www.ipam.ucla.edu/">Institute for Pure and Applied Mathematics</a> at the <a href="http://www.ucla.edu/">University of California, Los Angeles</a>.</p>
<p>Over the course of the summer,
I worked alongside an American student and two Chinese students on a mathematical optimization project on the topic of finding optimal signal encoding
schemes for wireless networks. The research was performed in conjunction with researchers from <a href="http://www.huawei.com/en/">Huawei</a>, a large telecommunications company located in
Shenzhen, China. Specifically, my role was to code a Monte Carlo simulation for the network's physical layer in order to test
the efficacy of the various schemes. We presented our findings at the 2012 Joint Mathematics Meetings in Boston

